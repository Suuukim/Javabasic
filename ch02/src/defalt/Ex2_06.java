package defalt;

public class Ex2_06 {
	public static void main(String[] args) {
		// 변수의 타입
		
		int x = 100;						// 변수의 타입은 정수형
		double pi = 3.14;					// 변수의 타입은 실수형
		char ch = 'a';						// 변수의 타입은 문자
		String str = "abc";					// 변수의 타입은 문자열
		
		System.out.println(x);				// 값 = 100
		System.out.println(pi);				// 값 = 3.14
		System.out.println(ch);				// 값 = a
		System.out.println(str);			// 값 = abc
	}
}
