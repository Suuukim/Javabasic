package defalt;

public class Ex2_05 {
	public static void main(String[] args) {
		int x = 10;
		int y = 5;
		System.out.println(x+y);		// x의 값 10 + y의 값 5가 되어 결과값이 15가 나온다.
		System.out.println(x-y);		// x의 값 10 - y의 값 5가 되어 결과값이 5가 나온다.
		System.out.println(x*y);		// x의 값 10 * y의 값 5가 되어 결과값이 50이 나온다.
		System.out.println(x/y);		// x의 값 10 / y의 값 5가 되어 결과값이 2가 나온다.
	}
}
